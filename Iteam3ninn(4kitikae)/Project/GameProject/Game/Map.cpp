				//描画
				//m_img.Draw();
				break;
			case 1://表示しない制御
				if (stage2data[i][j] == 0)continue;
				t = stage2data[i][j];
				//画像切り抜き
				//m_img.SetRect(32 * t, 0, 32 * t + 32, 32);
				//表示サイズ設定
				//m_img.SetSize(MAP_TIP_SIZE, MAP_TIP_SIZE);
